import axios from '../../backendService.js';

interface ApiProduct {
  _id: string;
  name: string;
  price: number;
  img: string;
  description: string;
  isFavorite: boolean;
  forBeginners: boolean;
  isPetSafe: boolean;
}

interface Product {
  _id: string;
  name: string;
  price: number;
  img: string;
  description: string;
  isFavorite: boolean;
  forBeginners: boolean;
  isPetSafe: boolean;
}

const fetchAndProcessData = async (): Promise<Product[]> => {
  try {
    const response = await axios('/api/products/');
    const fetchedData: ApiProduct[] = await response.data;
    console.log(fetchedData, "fetched data//........................................")
    const formattedData: Product[] = fetchedData.map((product, index) => ({
      _id: product._id,
      id: index + 1,
      name: product.name,
      price: product.price,
      img: product.img,
      description: product.description,
      isFavorite: product.isFavorite,
      forBeginners: product.forBeginners,
      isPetSafe: product.isPetSafe,
    }));

    // Store the formatted data in local storage
    localStorage.setItem('items', JSON.stringify(formattedData));

    return formattedData;
  } catch (error) {
    console.error('Error fetching data:', error);
    return [];
  }
};

export default fetchAndProcessData;